(async function main () {
  const grids = input.split('\n\n').map(p => p.split('\n'));

  console.time('start')
  console.table({START: 'START'});

  for (let part2 of [false, true]) {
    let ans = 0

    for (let grid of grids) {
      const n = grid.length;
      const m = grid[0].length;

      for (let i = 0; i < m - 1; i++) {
        let l = i
        let r = i+1
        let bad = 0

        while (0 <= l && r < m) {
          for (let j = 0; j < n; j++) {
            if (grid[j][l] != grid[j][r]) {
              bad++
            }

            if (bad > part2) break
          }

          if (bad > part2) break
          l--
          r++
        }

        if (bad == part2) ans += i + 1
      }

      for (let i = 0; i < n - 1; i++) {
        let l = i
        let r = i+1
        let bad = 0

        while (0 <= l && r < n) {
          for (let j = 0; j < m; j++) {
            if (grid[l][j] != grid[r][j]) {
              bad++
            }

            if (bad > part2) break
          }

          if (bad > part2) break
          l--
          r++
        }

        if (bad == part2) ans += 100*(i + 1)
      }
    }

    console.log(ans);
  }

  console.timeEnd('start')
})()
