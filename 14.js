(async function main () {
  const grid = input.split('\n').map(line => line.split(''))
  const n = grid.length

  console.log('---------')
  console.log('|       |')
  console.log('| START |')
  console.log('|       |')
  console.log('---------')
  console.time('start')

  for (let part2 of [false]) {
    const hash = {}
    const anses = {}
    let ans = 0
    let cycle = 0

    const tilts = [
      [0, n, true, 1],
      [0, n, false, 1],
      [n-1, -1, true, -1],
      [n-1, -1, false, -1],
    ]

    for (let w = 1; w < 1000000000; w++) {
      for (let t = 0; t < tilts.length; t++) {
        for (let i = 0; i < n; i++) {
          let [j, l, vertical, sign] = tilts[t]
          let pos = j

          for (; j != l; j += sign) {
            const rock = vertical ? grid[j][i] : grid[i][j]

            if (rock == '#') {
              pos = j + sign
            }

            if (rock == 'O') {
              if (vertical) {
                grid[j][i] = '.'
                grid[pos][i] = 'O'
              } else {
                grid[i][j] = '.'
                grid[i][pos] = 'O'
              }
              pos += sign
            }
          }
        }
      }

      const shot = grid.map(row => row.join('')).join('')

      if (hash[shot]) {
        cycle = [hash[shot], w];
        break;
      } else hash[shot] = w

      map = Array(n).fill(0)

      for (let i = 0; i < n; i++) {
        for (let j = 0; j < n; j++) {
          if (grid[i][j] === 'O') {
            map[i] += 1
          }
        }
      }

      ans = map.reduce((acc, v, i) => v ? acc + (n - i) * v : acc, 0)

      anses[w] = ans
    }

    console.log(anses[(1000000000 - cycle[0] + 1)%(cycle[1] - cycle[0]) + cycle[0] - 1])

  }
  console.timeEnd('start')
})()
